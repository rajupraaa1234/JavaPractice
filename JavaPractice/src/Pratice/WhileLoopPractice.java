package Pratice;

public class WhileLoopPractice {

	public static void main(String[] args) {
		    int i=1;  
		    while(i<=10){  
		        System.out.println(i);  
		    i++;  
		    }  
		    
		    int j=1;    
		    do{    
		        System.out.println(j);    
		    j++;    
		    }while(j<=10);    
		    
		    int k=1;
		    while(k<=15) {
		    	if(k==8) {
		    		break;
		    	}
		    	System.out.println(k+ " ");
		    	k++;
		    }

	}

}
